#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = ac++;
	while (i--, av[i] && i > 0)
	{
		j = -1;
		while (j++, av[i][j])
		{
			write(1, &av[i][j], 1);
		}
		write(1, "\n", 1);
	}
}
