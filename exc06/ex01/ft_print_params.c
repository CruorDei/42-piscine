#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 0;
	while (i++, av[i] && i < ac)
	{
		j = -1;
		while (j++, av[i][j])
			write(1, &av[i][j], 1);
		write(1, "\n", 1);
	}
	return (0);
}
